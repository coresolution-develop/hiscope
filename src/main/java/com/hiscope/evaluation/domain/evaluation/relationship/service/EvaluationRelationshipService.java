package com.hiscope.evaluation.domain.evaluation.relationship.service;

import com.hiscope.evaluation.common.exception.BusinessException;
import com.hiscope.evaluation.common.exception.ErrorCode;
import com.hiscope.evaluation.common.security.SecurityUtils;
import com.hiscope.evaluation.domain.employee.entity.Employee;
import com.hiscope.evaluation.domain.employee.repository.EmployeeRepository;
import com.hiscope.evaluation.domain.evaluation.relationship.dto.RelationshipManualRequest;
import com.hiscope.evaluation.domain.evaluation.relationship.entity.EvaluationRelationship;
import com.hiscope.evaluation.domain.evaluation.relationship.repository.EvaluationRelationshipRepository;
import com.hiscope.evaluation.domain.evaluation.session.entity.EvaluationSession;
import com.hiscope.evaluation.domain.evaluation.session.repository.EvaluationSessionRepository;
import lombok.RequiredArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.domain.Specification;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;
import org.springframework.util.StringUtils;

import java.util.*;
import java.util.stream.Collectors;

@Slf4j
@Service
@RequiredArgsConstructor
@Transactional(readOnly = true)
public class EvaluationRelationshipService {

    private final EvaluationRelationshipRepository relationshipRepository;
    private final EvaluationSessionRepository sessionRepository;
    private final EmployeeRepository employeeRepository;

    public List<EvaluationRelationship> findBySession(Long orgId, Long sessionId) {
        SecurityUtils.checkOrgAccess(orgId);
        validateSessionOwner(orgId, sessionId);
        return relationshipRepository.findBySessionIdOrderByRelationTypeAscEvaluatorIdAsc(sessionId);
    }

    public Page<EvaluationRelationship> findPageBySession(Long orgId, Long sessionId, Pageable pageable) {
        return findPageBySession(orgId, sessionId, new RelationshipFilter(null, null, null, null), pageable);
    }

    public Page<EvaluationRelationship> findPageBySession(Long orgId,
                                                          Long sessionId,
                                                          RelationshipFilter filter,
                                                          Pageable pageable) {
        SecurityUtils.checkOrgAccess(orgId);
        validateSessionOwner(orgId, sessionId);
        return relationshipRepository.findAll(buildFilterSpec(orgId, sessionId, filter), pageable);
    }

    public RelationshipCountSummary countSummary(Long orgId, Long sessionId) {
        return countSummary(orgId, sessionId, new RelationshipFilter(null, null, null, null));
    }

    public RelationshipCountSummary countSummary(Long orgId, Long sessionId, RelationshipFilter filter) {
        SecurityUtils.checkOrgAccess(orgId);
        validateSessionOwner(orgId, sessionId);
        if (!hasFilter(filter)) {
            long total = relationshipRepository.countBySessionId(sessionId);
            long active = relationshipRepository.countBySessionIdAndActive(sessionId, true);
            long autoGenerated = relationshipRepository.countBySessionIdAndSource(sessionId, "AUTO_GENERATED");
            long manualAdded = relationshipRepository.countBySessionIdAndSource(sessionId, "ADMIN_ADDED");
            Map<String, Long> relationTypeCountMap = relationshipRepository.countByRelationType(sessionId).stream()
                    .collect(Collectors.toMap(
                            row -> String.valueOf(row[0]),
                            row -> (Long) row[1]
                    ));
            return new RelationshipCountSummary(total, active, autoGenerated, manualAdded, relationTypeCountMap);
        }

        Specification<EvaluationRelationship> baseSpec = buildFilterSpec(orgId, sessionId, filter);
        long total = relationshipRepository.count(baseSpec);
        long active = relationshipRepository.count(baseSpec.and((root, query, cb) -> cb.isTrue(root.get("active"))));
        long autoGenerated = relationshipRepository.count(baseSpec.and((root, query, cb) -> cb.equal(root.get("source"), "AUTO_GENERATED")));
        long manualAdded = relationshipRepository.count(baseSpec.and((root, query, cb) -> cb.equal(root.get("source"), "ADMIN_ADDED")));
        Map<String, Long> relationTypeCountMap = relationshipRepository.findAll(baseSpec).stream()
                .collect(Collectors.groupingBy(EvaluationRelationship::getRelationType, Collectors.counting()));
        return new RelationshipCountSummary(total, active, autoGenerated, manualAdded, relationTypeCountMap);
    }

    private Specification<EvaluationRelationship> buildFilterSpec(Long orgId, Long sessionId, RelationshipFilter filter) {
        List<Long> keywordEmployeeIds = resolveEmployeeIdsByKeyword(orgId, filter.keyword());
        return (root, query, cb) -> {
            var predicates = new ArrayList<jakarta.persistence.criteria.Predicate>();
            predicates.add(cb.equal(root.get("organizationId"), orgId));
            predicates.add(cb.equal(root.get("sessionId"), sessionId));
            if (StringUtils.hasText(filter.relationType())) {
                predicates.add(cb.equal(root.get("relationType"), filter.relationType()));
            }
            if (StringUtils.hasText(filter.source())) {
                predicates.add(cb.equal(root.get("source"), filter.source()));
            }
            if (filter.active() != null) {
                predicates.add(cb.equal(root.get("active"), filter.active()));
            }
            if (keywordEmployeeIds != null) {
                if (keywordEmployeeIds.isEmpty()) {
                    return cb.disjunction();
                }
                predicates.add(cb.or(
                        root.get("evaluatorId").in(keywordEmployeeIds),
                        root.get("evaluateeId").in(keywordEmployeeIds)
                ));
            }
            return cb.and(predicates.toArray(new jakarta.persistence.criteria.Predicate[0]));
        };
    }

    private List<Long> resolveEmployeeIdsByKeyword(Long orgId, String keyword) {
        if (!StringUtils.hasText(keyword)) {
            return null;
        }
        return employeeRepository.findIdsByOrganizationIdAndKeyword(orgId, keyword.trim());
    }

    private boolean hasFilter(RelationshipFilter filter) {
        return StringUtils.hasText(filter.keyword())
                || StringUtils.hasText(filter.relationType())
                || StringUtils.hasText(filter.source())
                || filter.active() != null;
    }

    /**
     * 평가 관계 자동 생성
     *
     * 규칙:
     *  UPWARD  : 팀원 직책(비팀장) → 같은 부서 팀장
     *  DOWNWARD: 팀장 직책 → 같은 부서 팀원
     *  PEER    : 같은 부서 내 동료 직원 (팀원 ↔ 팀원)
     *
     * 기존 AUTO_GENERATED 관계는 모두 비활성화 후 재생성.
     * ADMIN_ADDED 관계는 영향 없음. (수동 추가가 자동보다 우선)
     */
    @Transactional
    public int autoGenerate(Long orgId, Long sessionId) {
        SecurityUtils.checkOrgAccess(orgId);
        EvaluationSession session = validateSessionOwner(orgId, sessionId);

        if (!session.isPending()) {
            throw new BusinessException(ErrorCode.SESSION_ALREADY_STARTED,
                    "평가가 시작된 후에는 자동 생성할 수 없습니다.");
        }

        // 기존 AUTO_GENERATED 비활성화
        relationshipRepository.findBySessionIdOrderByRelationTypeAscEvaluatorIdAsc(sessionId)
                .stream()
                .filter(EvaluationRelationship::isAutoGenerated)
                .forEach(EvaluationRelationship::deactivate);

        // ADMIN_ADDED 관계 기존 쌍 수집 (중복 방지)
        Set<String> existingPairs = relationshipRepository
                .findBySessionIdOrderByRelationTypeAscEvaluatorIdAsc(sessionId)
                .stream()
                .filter(r -> r.isManualAdded() && r.isActive())
                .map(r -> r.getEvaluatorId() + "_" + r.getEvaluateeId())
                .collect(Collectors.toSet());

        List<Employee> employees = employeeRepository.findByOrganizationIdAndStatusOrderByNameAsc(orgId, "ACTIVE");

        // 부서별 그룹핑
        Map<Long, List<Employee>> byDept = employees.stream()
                .filter(e -> e.getDepartmentId() != null)
                .collect(Collectors.groupingBy(Employee::getDepartmentId));

        List<EvaluationRelationship> toSave = new ArrayList<>();

        for (Map.Entry<Long, List<Employee>> entry : byDept.entrySet()) {
            List<Employee> deptEmployees = entry.getValue();
            List<Employee> leaders = deptEmployees.stream().filter(Employee::isTeamLeader).toList();
            List<Employee> members = deptEmployees.stream().filter(e -> !e.isTeamLeader()).toList();

            // UPWARD: 팀원 → 각 팀장
            for (Employee member : members) {
                for (Employee leader : leaders) {
                    addIfNotExists(toSave, existingPairs, sessionId, orgId,
                            member.getId(), leader.getId(), "UPWARD");
                }
            }

            // DOWNWARD: 팀장 → 각 팀원
            for (Employee leader : leaders) {
                for (Employee member : members) {
                    addIfNotExists(toSave, existingPairs, sessionId, orgId,
                            leader.getId(), member.getId(), "DOWNWARD");
                }
            }

            // PEER: 팀원 ↔ 팀원 (양방향)
            for (int i = 0; i < members.size(); i++) {
                for (int j = i + 1; j < members.size(); j++) {
                    Employee a = members.get(i);
                    Employee b = members.get(j);
                    addIfNotExists(toSave, existingPairs, sessionId, orgId,
                            a.getId(), b.getId(), "PEER");
                    addIfNotExists(toSave, existingPairs, sessionId, orgId,
                            b.getId(), a.getId(), "PEER");
                }
            }
        }

        relationshipRepository.saveAll(toSave);
        log.info("Auto-generated {} evaluation relationships for session {}", toSave.size(), sessionId);
        return toSave.size();
    }

    private void addIfNotExists(List<EvaluationRelationship> list, Set<String> existingPairs,
                                Long sessionId, Long orgId,
                                Long evaluatorId, Long evaluateeId, String type) {
        String key = evaluatorId + "_" + evaluateeId;
        if (evaluatorId.equals(evaluateeId)) return; // 자기평가 방지
        if (existingPairs.contains(key)) return;
        existingPairs.add(key);
        list.add(EvaluationRelationship.builder()
                .sessionId(sessionId).organizationId(orgId)
                .evaluatorId(evaluatorId).evaluateeId(evaluateeId)
                .relationType(type).source("AUTO_GENERATED").active(true)
                .build());
    }

    /**
     * 관리자 수동 관계 추가
     * ADMIN_ADDED 관계는 AUTO_GENERATED 관계보다 우선하며, 자동 생성 시 영향 없음.
     */
    @Transactional
    public EvaluationRelationship addManual(Long orgId, Long sessionId, RelationshipManualRequest request) {
        SecurityUtils.checkOrgAccess(orgId);
        EvaluationSession session = validateSessionOwner(orgId, sessionId);

        if (session.isClosed()) {
            throw new BusinessException(ErrorCode.SESSION_CLOSED);
        }
        if (request.getEvaluatorId().equals(request.getEvaluateeId())) {
            throw new BusinessException(ErrorCode.SELF_EVALUATION_NOT_ALLOWED);
        }

        // 이미 활성화된 관계가 있으면 충돌
        Optional<EvaluationRelationship> existing = relationshipRepository
                .findBySessionIdAndEvaluatorIdAndEvaluateeId(
                        sessionId, request.getEvaluatorId(), request.getEvaluateeId());

        if (existing.isPresent()) {
            EvaluationRelationship rel = existing.get();
            if (rel.isActive()) {
                throw new BusinessException(ErrorCode.RELATIONSHIP_DUPLICATE);
            }
            // 비활성화된 AUTO_GENERATED가 있으면 → ADMIN_ADDED로 재활성화
            rel.activate();
            return rel;
        }

        // 피평가자 소유권 검증
        employeeRepository.findByOrganizationIdAndId(orgId, request.getEvaluateeId())
                .orElseThrow(() -> new BusinessException(ErrorCode.EMPLOYEE_NOT_FOUND));
        employeeRepository.findByOrganizationIdAndId(orgId, request.getEvaluatorId())
                .orElseThrow(() -> new BusinessException(ErrorCode.EMPLOYEE_NOT_FOUND));

        EvaluationRelationship rel = EvaluationRelationship.builder()
                .sessionId(sessionId).organizationId(orgId)
                .evaluatorId(request.getEvaluatorId())
                .evaluateeId(request.getEvaluateeId())
                .relationType("MANUAL").source("ADMIN_ADDED").active(true)
                .build();
        return relationshipRepository.save(rel);
    }

    public EvaluationRelationship findById(Long orgId, Long relationshipId) {
        SecurityUtils.checkOrgAccess(orgId);
        return relationshipRepository.findByOrganizationIdAndId(orgId, relationshipId)
                .orElseThrow(() -> new BusinessException(ErrorCode.RELATIONSHIP_NOT_FOUND));
    }

    /**
     * 관계 비활성화/삭제
     * AUTO_GENERATED → is_active = false (이력 보존)
     * ADMIN_ADDED    → 실제 삭제
     */
    @Transactional
    public void remove(Long orgId, Long sessionId, Long relationshipId) {
        SecurityUtils.checkOrgAccess(orgId);
        EvaluationSession session = validateSessionOwner(orgId, sessionId);

        if (!session.isPending()) {
            throw new BusinessException(ErrorCode.SESSION_ALREADY_STARTED,
                    "평가가 시작된 후에는 관계를 삭제할 수 없습니다.");
        }

        EvaluationRelationship rel = relationshipRepository.findByOrganizationIdAndId(orgId, relationshipId)
                .orElseThrow(() -> new BusinessException(ErrorCode.RELATIONSHIP_NOT_FOUND));

        if (!rel.getSessionId().equals(sessionId)) {
            throw new BusinessException(ErrorCode.FORBIDDEN);
        }

        if (rel.isManualAdded()) {
            relationshipRepository.delete(rel);
        } else {
            rel.deactivate();
        }
    }

    private EvaluationSession validateSessionOwner(Long orgId, Long sessionId) {
        return sessionRepository.findByOrganizationIdAndId(orgId, sessionId)
                .orElseThrow(() -> new BusinessException(ErrorCode.SESSION_NOT_FOUND));
    }

    public record RelationshipCountSummary(
            long total,
            long active,
            long autoGenerated,
            long manualAdded,
            Map<String, Long> relationTypeCountMap
    ) {
    }

    public record RelationshipFilter(
            String keyword,
            String relationType,
            String source,
            Boolean active
    ) {
    }
}
