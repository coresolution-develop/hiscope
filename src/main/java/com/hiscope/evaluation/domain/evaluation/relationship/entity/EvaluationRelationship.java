package com.hiscope.evaluation.domain.evaluation.relationship.entity;

import jakarta.persistence.*;
import lombok.*;

import java.time.LocalDateTime;

/**
 * 평가 관계 테이블
 *
 * source: AUTO_GENERATED (자동 생성) | ADMIN_ADDED (관리자 수동 추가)
 * is_active: false = 관리자가 비활성화. AUTO_GENERATED는 is_active 토글, ADMIN_ADDED는 삭제
 *
 * 설계 원칙:
 *  - AUTO_GENERATED 관계는 is_active=false로 비활성화 (이력 보존)
 *  - ADMIN_ADDED 관계는 실제 삭제 허용
 *  - Assignment 생성 시 is_active=true인 관계만 사용
 */
@Entity
@Table(name = "evaluation_relationships",
        uniqueConstraints = @UniqueConstraint(columnNames = {"session_id", "evaluator_id", "evaluatee_id"}))
@Getter
@NoArgsConstructor(access = AccessLevel.PROTECTED)
@AllArgsConstructor
@Builder
public class EvaluationRelationship {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(name = "session_id", nullable = false)
    private Long sessionId;

    @Column(name = "organization_id", nullable = false)
    private Long organizationId;

    @Column(name = "evaluator_id", nullable = false)
    private Long evaluatorId;

    @Column(name = "evaluatee_id", nullable = false)
    private Long evaluateeId;

    /**
     * UPWARD: 직원 → 상사(팀장) 평가
     * DOWNWARD: 팀장 → 직원 평가
     * PEER: 동료 상호 평가
     * CROSS_DEPT: 부서 간 평가
     * MANUAL: 관리자 임의 추가
     */
    @Column(name = "relation_type", nullable = false, length = 20)
    private String relationType;

    @Column(name = "resolved_question_group_code", length = 30)
    private String resolvedQuestionGroupCode;

    /** AUTO_GENERATED | ADMIN_ADDED */
    @Column(name = "source", nullable = false, length = 20)
    @Builder.Default
    private String source = "AUTO_GENERATED";

    @Column(name = "is_active", nullable = false)
    @Builder.Default
    private boolean active = true;

    @Column(name = "created_at", nullable = false)
    @Builder.Default
    private LocalDateTime createdAt = LocalDateTime.now();

    public void deactivate() {
        this.active = false;
    }

    public void activate() {
        this.active = true;
    }

    public boolean isManualAdded() {
        return "ADMIN_ADDED".equals(this.source);
    }

    public boolean isAutoGenerated() {
        return "AUTO_GENERATED".equals(this.source);
    }

    public void applyResolvedQuestionGroupCode(String resolvedQuestionGroupCode) {
        this.resolvedQuestionGroupCode = resolvedQuestionGroupCode;
    }
}
